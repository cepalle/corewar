#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	len1;
	size_t	len2;
	char	*to_do;

	if (!s1 || !s2)
		return (NULL);
	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	if (!(to_do = ft_strnew(len1 + len2)))
		return (NULL);
	ft_strcpy(to_do, s1);
	ft_strcpy(to_do + len1, s2);
	return (to_do);
}
